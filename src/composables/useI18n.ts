const currentLanguage = ref<'en' | 'nl'>((localStorage.getItem('language') as 'en' | 'nl') || 'en')

export function useI18n() {
  function t(key: string): string {
    return translations[currentLanguage.value]?.[key] || translations['en']?.[key] || key
  }

  function setLanguage(lang: 'en' | 'nl') {
    currentLanguage.value = lang
    localStorage.setItem('language', lang)
    document.documentElement.setAttribute('lang', lang)
  }

  function toggleLanguage() {
    setLanguage(currentLanguage.value === 'en' ? 'nl' : 'en')
  }

  return { t, currentLanguage: computed(() => currentLanguage.value), setLanguage, toggleLanguage }
}
